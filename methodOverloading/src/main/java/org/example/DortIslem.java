package org.example;

public class DortIslem {
    // İlk topla metodu, iki tamsayıyı toplar ve sonucu döner
    public int topla(int sayi1, int sayi2) {
        return sayi1 + sayi2;
    }

    // İkinci topla metodu, üç tamsayıyı toplar ve sonucu döner
    public int topla(int sayi1, int sayi2, int sayi3) {
        return sayi1 + sayi2 + sayi3;
    }
}
